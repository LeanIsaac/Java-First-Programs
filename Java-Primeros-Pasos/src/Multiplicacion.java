
public class Multiplicacion {

	public static void main(String[] args) {
		
		for(int cont=0; cont <= 10 ; cont++) {
			for(int mult=0; mult <= 10; mult++) {
				int result= cont * mult;
				
				System.out.print(result);
				System.out.print(" ");
			}
			System.out.println();
		}
		
	}//FIN METODO MAIN // FIN SCOPE
}
